def escape_html(value):
    text = repo_relative_text(value)
    return text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")


def summarize_fix(findings):
    actionable = [item for item in findings if item.get("severity") != "Info"][:12]
    lines = []
    for item in actionable:
        fix = repo_relative_text(item.get("fix", "Analyze the root cause and harden it sustainably."))
        lines.append(
            f"- [{item.get('severity', 'Info')}] {repo_relative_text(item.get('title', ''))} at "
            f"{repo_relative_text(item.get('target', ''))}: {fix}"
        )
    return "\n".join(lines) if lines else "- No non-informational findings were recorded."


def build_agent_prompt(target, findings):
    return "\n".join(
        [
            "Du bist ein Coding-Agent und sollst die folgenden Sicherheitsprobleme beheben.",
            f"Zielsystem: {repo_relative_text(target)}",
            "",
            "Vorgehen:",
            "1. Gruppiere die Findings nach gemeinsamer technischer Ursache.",
            "2. Behebe zuerst Critical-, High- und Medium-Findings im Code oder in der Server-/Deployment-Konfiguration.",
            "3. Entferne keine Schutzmechanismen ohne gleichwertigen Ersatz.",
            "4. Ergaenze oder aktualisiere Tests, damit die Probleme nicht wieder auftreten.",
            "5. Erklaere am Ende, welches Finding durch welche Aenderung geschlossen wird.",
            "",
            "Findings und technische Behebungsrichtung:",
            summarize_fix(findings),
            "",
            "Erwartete Ausgabe:",
            "- geaenderte Dateien",
            "- kurze Zusammenfassung pro Aenderung",
            "- welche Findings dadurch behoben sind",
            "- verbleibende Risiken oder manuelle Nachtests",
        ]
    )


def main():
    run_meta = current_run_meta()
    reports_dir = pathlib.Path("reports")
    reports_dir.mkdir(exist_ok=True)
    scoped_reports_dir = run_reports_dir(run_meta.get("run_id"))
    all_findings = []
    target = run_meta.get("target", "(multi)")

    for file_path in iter_run_json(latest_only=True):
        try:
            data = json.loads(file_path.read_text(encoding="utf-8"))
            target = data.get("target", target)
            for item in data.get("findings", []):
                item.setdefault("severity", "Info")
                item["_script"] = data.get("script", "")
                all_findings.append(item)
        except Exception:
            continue

    all_findings.sort(key=lambda item: SEV_ORDER.get(item.get("severity", "Info"), 99))
    counter = Counter(item.get("severity", "Info") for item in all_findings)

    summary_cards = "".join(
        f'<div class="sev-card" style="border-color:{SEV_COLORS[severity]}"><div class="n" style="color:{SEV_COLORS[severity]}">{counter.get(severity, 0)}</div><div class="l">{severity}</div></div>'
        for severity in ["Critical", "High", "Medium", "Low", "Info"]
    )
    filter_buttons = "".join(
        f'<div class="filter" data-f="{severity}">{severity} ({counter.get(severity, 0)})</div>'
        for severity in ["Critical", "High", "Medium", "Low", "Info"]
        if counter.get(severity, 0) > 0
    )

    findings_html = []
    for finding in all_findings:
        severity = finding.get("severity", "Info")
        exploit_status = finding.get("exploitability_status")
        meta = "".join(
            f'<span class="badge-meta">{escape_html(key)}: {escape_html(finding[key])}</span>'
            for key in ("cwe", "owasp", "cvss", "_script", "id")
            if finding.get(key)
        )
        if exploit_status:
            meta = f'<span class="badge-meta">Exploitability: {escape_html(exploit_status)}</span>' + meta
        body_dl = "".join(
            f'<dt>{escape_html(key.replace("_", " ").title())}</dt><dd>{escape_html(value)}</dd>'
            for key, value in finding.items()
            if key in ("target", "description", "impact", "fix", "evidence", "references", "exploitability_status", "attacker_requirements", "attack_path", "affected_findings") and value
        )
        reproduction = ""
        if finding.get("reproduction"):
            steps = "".join(f"<li>{escape_html(step)}</li>" for step in finding["reproduction"])
            reproduction = f"<dt>Reproduction</dt><dd><ol>{steps}</ol></dd>"

        findings_html.append(
            f"""
        <div class="finding" data-sev="{severity}">
          <div class="f-head">
            <div class="f-title">{escape_html(finding.get("title", "?"))}</div>
            <div class="f-sev" style="background:{SEV_COLORS[severity]}">{severity}</div>
          </div>
          <div class="f-body">
            <div style="margin-top:12px">{meta}</div>
            <dl>{body_dl}{reproduction}</dl>
          </div>
        </div>"""
        )

    agent_prompt = build_agent_prompt(target, all_findings)
    html = TEMPLATE.format(
        target=escape_html(target),
        date=datetime.now().strftime("%Y-%m-%d %H:%M"),
        total=len(all_findings),
        agent_prompt=escape_html(agent_prompt),
        summary_cards=summary_cards,
        filter_buttons=filter_buttons,
        findings_html="".join(findings_html) or '<p style="color:#94a3b8;text-align:center;padding:40px">No findings</p>',
    )

    timestamp = datetime.now().strftime("%Y-%m-%d_%H%M")
    run_id = run_meta.get("run_id", "ad-hoc")
    out_file = reports_dir / f"{run_id}_{timestamp}.html"
    scoped_out_file = scoped_reports_dir / "report.html"
    prompt_file = reports_dir / f"{run_id}_{timestamp}_coding_agent_prompt.txt"
    scoped_prompt_file = scoped_reports_dir / "coding_agent_prompt.txt"

    out_file.write_text(html, encoding="utf-8")
    scoped_out_file.write_text(html, encoding="utf-8")
    prompt_file.write_text(agent_prompt + "\n", encoding="utf-8")
    scoped_prompt_file.write_text(agent_prompt + "\n", encoding="utf-8")
    print(f"[+] Report -> {out_file}")


if __name__ == "__main__":
    main()
